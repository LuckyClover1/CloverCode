package com.clover.enumEntity;

/**
 * @author Clover
 */
public class Author {
    private int authorId;
    private String authorName;
    private String authorHost;
    private String sex;
    private int age;
    private String haoxiaoNum;
    private String fansNum;
    private String qiushiNum;

    public int getAuthorId() {
        return authorId;
    }

    public void setAuthorId(int authorId) {
        this.authorId = authorId;
    }

    public String getAuthorName() {
        return authorName;
    }

    public void setAuthorName(String authorName) {
        this.authorName = authorName;
    }

    public String getAuthorHost() {
        return authorHost;
    }

    public void setAuthorHost(String authorHost) {
        this.authorHost = authorHost;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getHaoxiaoNum() {
        return haoxiaoNum;
    }

    public void setHaoxiaoNum(String haoxiaoNum) {
        this.haoxiaoNum = haoxiaoNum;
    }

    public String getFansNum() {
        return fansNum;
    }

    public void setFansNum(String fansNum) {
        this.fansNum = fansNum;
    }

    public String getQiushiNum() {
        return qiushiNum;
    }

    public void setQiushiNum(String qiushiNum) {
        this.qiushiNum = qiushiNum;
    }

}
